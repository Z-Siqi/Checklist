package sqz.checklist.common

object UrlRegexHelper {

    /**
     * Match IPv4 address
     *
     * Note: this regex will not include matches of
     * `http` or `/` character; e.g. `http://1.1.1.1/` or `1.1.1.1/xxx`
     */
    val ipv4Regex = Regex(
        """^(?:25[0-5]|2[0-4]\d|1\d{2}|[1-9]\d|\d)(?:\.(?:25[0-5]|2[0-4]\d|1\d{2}|[1-9]\d|\d)){3}$"""
    )

    /**
     * Match IPv4 URL
     */
    val ipv4UrlRegex = Regex(
        """^https?://(?:25[0-5]|2[0-4]\d|1\d{2}|[1-9]\d|\d)(?:\.(?:25[0-5]|2[0-4]\d|1\d{2}|[1-9]\d|\d)){3}(?::\d+)?(?:/.*)?$"""
    )

    /**
     * Match IPv6 URL
     */
    val ipv6UrlRegex = Regex(
        """^[A-Za-z][A-Za-z0-9+.\-]*://""" +
                """(?:[A-Za-z0-9._~!$&'()*+,;=:%\-]+@)?""" +
                """\[""" +
                """(?:(?:[0-9A-Fa-f]{1,4}:){7}[0-9A-Fa-f]{1,4}|""" +
                """(?:[0-9A-Fa-f]{1,4}:){1,7}:|""" +
                """(?:[0-9A-Fa-f]{1,4}:){1,6}:[0-9A-Fa-f]{1,4}|""" +
                """(?:[0-9A-Fa-f]{1,4}:){1,5}(?::[0-9A-Fa-f]{1,4}){1,2}|""" +
                """(?:[0-9A-Fa-f]{1,4}:){1,4}(?::[0-9A-Fa-f]{1,4}){1,3}|""" +
                """(?:[0-9A-Fa-f]{1,4}:){1,3}(?::[0-9A-Fa-f]{1,4}){1,4}|""" +
                """(?:[0-9A-Fa-f]{1,4}:){1,2}(?::[0-9A-Fa-f]{1,4}){1,5}|""" +
                """[0-9A-Fa-f]{1,4}:(?::[0-9A-Fa-f]{1,4}){1,6}|""" +
                """:(?::[0-9A-Fa-f]{1,4}){1,7}|::|""" +
                """(?:[0-9A-Fa-f]{1,4}:){6}(?:25[0-5]|2[0-4]\d|1?\d?\d)(?:\.(?:25[0-5]|2[0-4]\d|1?\d?\d)){3}|""" +
                """(?:[0-9A-Fa-f]{1,4}:){0,5}:(?:25[0-5]|2[0-4]\d|1?\d?\d)(?:\.(?:25[0-5]|2[0-4]\d|1?\d?\d)){3}|""" +
                """::(?:[0-9A-Fa-f]{1,4}:){0,5}(?:25[0-5]|2[0-4]\d|1?\d?\d)(?:\.(?:25[0-5]|2[0-4]\d|1?\d?\d)){3})""" +
                """(?:%25[0-9A-Za-z._~\-]+)?""" +
                """]""" +
                """(?::\d{1,5})?""" +
                """(?:[/?#]\S*)?$""",
        RegexOption.IGNORE_CASE
    )

    /**
     * Match URL
     *
     * Note: this regex will not include matches of IPv4 and IPv6 address: e.g. `http://1.1.1.1/`
     */
    val urlRegex = Regex(
        """^(?!(?:\d{1,3}\.){3}\d{1,3}(?::\d+)?(?:/|$))""" +
                """(?:(?:https?|ftp)://)?(?:[\p{L}\p{N}]+(?:-[\p{L}\p{N}]+)*\.)+\p{L}{2,}(?::\d+)?""" +
                """(?:[/?#]\S*)?$""",
        RegexOption.IGNORE_CASE
    )
}
